
/**
 * Enum representing different types of abilities
 */
public enum AbilityType {
    DAMAGE,        // Deals damage to opponent
    HEAL,          // Restores HP
    EP_RESTORE,    // Restores EP
    DEFEND,        // Defensive stance
    RECHARGE,      // Basic recharge action
    SHIELD,        // Complete damage immunity
    EVASION,       // Chance to evade attacks
    SNEAK_ATTACK   // Special rogue attack with evasion
}